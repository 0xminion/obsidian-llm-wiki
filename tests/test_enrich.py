# ─── enrich_note — frontmatter edge cases ───────────────────────────────────


class TestEnrichNoteFrontmatterEdgeCases:
    @patch("pipeline.enrich.extract_url")
    def test_source_url_with_surrounding_quotes(self, mock_extract_url, tmp_path, cfg, mock_client):
        """Quoted URL values in frontmatter are stripped correctly."""
        note = tmp_path / "quoted-url.md"
        note.write_text(
            '---\ntitle: Quoted\nsource: "https://example.com/quoted"\n---\n\nBody.\n',
            encoding="utf-8",
        )

        mock_extract_url.return_value = ExtractedSource(
            url="https://example.com/quoted",
            title="Quoted",
            content="New body.\n",
        )

        result = enrich_note(note, cfg, client=mock_client)

        # extract_url should be called with the unquoted URL
        mock_extract_url.assert_called_once()
        call_url = mock_extract_url.call_args[0][0]
        assert call_url == "https://example.com/quoted"
        assert result["status"] == "plan_generated"

    @patch("pipeline.enrich.extract_url")
    def test_title_fallback_to_stem(self, mock_extract_url, tmp_path, cfg, mock_client):
        """When frontmatter has no title, note_path.stem is used in the prompt."""
        note = tmp_path / "no-title-note.md"
        note.write_text(
            "---\nsource: https://example.com/x\n---\n\nBody.\n",
            encoding="utf-8",
        )

        mock_extract_url.return_value = ExtractedSource(
            url="https://example.com/x",
            title="New Title",
            content="Changed body.\n",
        )

        enrich_note(note, cfg, client=mock_client)

        # The prompt should contain the stem as existing title
        prompt_arg = mock_client.generate.call_args[0][0]
        assert "no-title-note" in prompt_arg

    def test_no_frontmatter_at_all(self, tmp_path, cfg):
        """A note with no YAML frontmatter returns error (no source URL)."""
        note = tmp_path / "bare.md"
        note.write_text("Just some plain text, no frontmatter.\n", encoding="utf-8")

        result = enrich_note(note, cfg)

        assert result["status"] == "error"
        assert "No source URL" in result["error"]


